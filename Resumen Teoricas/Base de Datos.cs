        Cadena de conexión:

            Una cadena de conexión contiene información de inicialización que se transfiere como un parámetro desde un proveedor de datos a un origen de datos. Suele ser un conjunto de claves y valores separados por punto y coma “;”. El conjunto de claves y valores esta conectado por el signo de igual por ejemplo clave1=valor1;clave2=valor2.

